      <h1>&nbsp;</h1>
      <h1>Data and data analysis</h1>
      <p>The ARMS-MBON data flow is described in the Data Management Plan. All data collected by the consortium are subjected to a moratorium period, normally of a year, to allow time to ensure the data are quality controlled and for the partners to have a chance to analyse the data and publish results. After this period has elapsed, the data are open access.</p>
      <p>The data collected from the partners are intially managed via the consortium's account in the <a href="https://plutof.ut.ee/#/" target="_blank" rel="noopener noreferrer">PlutoF</a>&nbsp;platform (contact Matthias Obst for access to the ARMS account in PlutoF), and each year be copied to the <a href="https://www.marinedataarchive.org/" target="_blank" rel="noopener noreferrer">Marine Data Archive</a> for long-term preservation.&nbsp;The metadata record for all ARMS data will be hosted by&nbsp;<a href="http://vliz.be/en/integrated-marine-information-system" target="_blank" rel="noopener noreferrer">IMIS</a> (the datasets catalogue of VLIZ), in the&nbsp;<a href="http://www.assembleplus.eu/results/datasets" target="_blank">ASSEMBLE Plus data collection</a>: go directly&nbsp;<a href="http://www.assembleplus.eu/node/2047?module=dataset&amp;dasid=6405" target="_blank">here</a> to get to the ARMS-MBON master record. After standardisation into DwC formats and after the moratorium period, the data will be published on EurOBIS and GBIF.</p>
      <p>In close collaboration with LifeWatch ERIC and its&nbsp;Internal Joint Initiative on Non-Indigenous species,&nbsp;a data-analysis workflow for pipeline-processing the raw sequences and the images&nbsp;for ARMS is being&nbsp;developed. This&nbsp;workflow will combine&nbsp;of a number of essential reference tools and will allows for a standardised processing of the collected data. An important element in this data analysis workflow is <a href="https://academic.oup.com/gigascience/article/9/3/giaa022/5803335" target="_blank" rel="noopener noreferrer">P.E.M.A</a>, a Pipeline for Environmental DNA Metabarcoding Analysis.</p>
